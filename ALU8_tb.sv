`timescale 1ns/1ps

module ALU8_tb;

    // Inputs
    logic [7:0] A;
    logic [7:0] B;
    logic [2:0] Op;

    // Outputs
    logic [7:0] Result;
    logic Carry;
    logic Zero;
    logic Negative;
    logic Overflow;

ALU8 DUT (
    .A(A),
    .B(B),
    .Op(Op),
    .Result(Result),
    .Carry(Carry),
    .Zero(Zero),
    .Negative(Negative),
    .Overflow(Overflow)
);

    initial begin

        $display("==============================================");
        $display("      8-bit Arithmetic Logic Unit Test");
        $display("==============================================");

        $monitor("Time=%0t | Op=%b | A=%d | B=%d | Result=%d | Carry=%b | Zero=%b | Negative=%b | Overflow=%b",
                  $time, Op, A, B, Result, Carry, Zero, Negative, Overflow);

        //-------------------------------------------------
        // ADD
        //-------------------------------------------------
        A = 8'd10;
        B = 8'd5;
        Op = 3'b000;
        #10;

        //-------------------------------------------------
        // ADD Overflow
        //-------------------------------------------------
        A = 8'd127;
        B = 8'd1;
        Op = 3'b000;
        #10;

        //-------------------------------------------------
        // SUB
        //-------------------------------------------------
        A = 8'd20;
        B = 8'd8;
        Op = 3'b001;
        #10;

        //-------------------------------------------------
        // SUB Negative
        //-------------------------------------------------
        A = 8'd5;
        B = 8'd15;
        Op = 3'b001;
        #10;

        //-------------------------------------------------
        // AND
        //-------------------------------------------------
        A = 8'b10101010;
        B = 8'b11001100;
        Op = 3'b010;
        #10;

        //-------------------------------------------------
        // OR
        //-------------------------------------------------
        A = 8'b10101010;
        B = 8'b11001100;
        Op = 3'b011;
        #10;

        //-------------------------------------------------
        // XOR
        //-------------------------------------------------
        A = 8'b10101010;
        B = 8'b11001100;
        Op = 3'b100;
        #10;

        //-------------------------------------------------
        // NOT
        //-------------------------------------------------
        A = 8'b10101010;
        B = 8'b00000000;
        Op = 3'b101;
        #10;

        //-------------------------------------------------
        // Zero Test
        //-------------------------------------------------
        A = 8'd0;
        B = 8'd0;
        Op = 3'b000;
        #10;

        $display("==============================================");
        $display("Simulation Finished");
        $display("==============================================");

        $stop;

    end

endmodule
